              <strong>Rebus air hingga mendidih</strong>
              <p>Rebus air hingga mendidih selama minimal 10 menit. Ini efektif untuk membunuh bakteri, virus, dan parasit penyebab diare, tifoid, dan kolera. Namun perlu diingat: perebusan <strong>tidak menghilangkan logam berat, pestisida, atau mikroplastik</strong>.</p>
            </div>
          </div>
