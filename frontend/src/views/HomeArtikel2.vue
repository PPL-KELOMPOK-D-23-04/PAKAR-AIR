          (langsung terasa dalam hitungan jam atau hari) hingga kronis (muncul setelah bertahun-tahun terpapar). 
          Memahami risiko ini adalah langkah pertama untuk melindungi diri sendiri, keluarga, dan masyarakat.
        </p>

